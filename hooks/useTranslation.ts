export const getLanguage = (): Language => {
  if (typeof window === 'undefined') return 'es'
  const saved = localStorage.getItem('language') as Language
  return saved === 'en' ? 'en' : 'es'
}

export const setLanguage = (lang: Language): void => {
  if (typeof window === 'undefined') return
  localStorage.setItem('language', lang)
  window.dispatchEvent(new Event('languagechange'))
}

export const useTranslation = () => {
  const [language, setLang] = useState<Language>(getLanguage())

  useEffect(() => {
    const handleLanguageChange = () => {
      setLang(getLanguage())
    }
    window.addEventListener('languagechange', handleLanguageChange)
    return () => window.removeEventListener('languagechange', handleLanguageChange)
  }, [])

  const t = (key: string): string => {
    const translation = translations[key]
    if (!translation) {
      console.warn(`Translation missing for key: ${key}`)
      return key
    }
    return translation[language] || translation.es || key
  }

  return {
    t,
    language,
    setLanguage: (lang: Language) => {
      setLanguage(lang)
      setLang(lang)
    }
  }
}

/**
 * Helper function to get localized field value with fallback logic
 * If requested language field is blank, uses the other language for both
 * @param fieldEs - Spanish field value
 * @param fieldEn - English field value
 * @param language - Current language preference
 * @returns The appropriate field value based on language and fallback logic
 */
export const getLocalizedField = (fieldEs: string | null | undefined, fieldEn: string | null | undefined, language: Language): string => {
  const es = (fieldEs || '').trim()
  const en = (fieldEn || '').trim()
  
  // If both are blank, return empty string
  if (!es && !en) {
    return ''
  }
  
  // If requested language exists and is not blank, use it
  if (language === 'es' && es) {
    return es
  }
  if (language === 'en' && en) {
    return en
  }
  
  // If requested language is blank but other exists, use other for both
  if (language === 'es' && !es && en) {
    return en
  }
  if (language === 'en' && !en && es) {
    return es
  }
  
  // Fallback to non-empty value
  return es || en
}



